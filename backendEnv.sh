#!/bin/sh
echo "ENVIRONMENT VARIABLES, BACKEND:"

# DB: DATABASE_URL
echo "DATABASE_URL:"
read DATABASE_URL

# DB: DIRECT_URL
echo "DIRECT_URL:"
read DIRECT_URL

# FIREBASE: FIREBASE_TYPE
echo "FIREBASE_TYPE:"
read FIREBASE_TYPE

# FIREBASE: FIREBASE_PROJECT_ID
echo "FIREBASE_PROJECT_ID:"
read FIREBASE_PROJECT_ID

# FIREBASE: FIREBASE_PRIVATE_KEY_ID
echo "FIREBASE_PRIVATE_KEY_ID:"
read FIREBASE_PRIVATE_KEY_ID

# FIREBASE: FIREBASE_PRIVATE_KEY
echo "FIREBASE_PRIVATE_KEY:"
read FIREBASE_PRIVATE_KEY

# FIREBASE: FIREBASE_CLIENT_EMAIL
echo "FIREBASE_CLIENT_EMAIL:"
read FIREBASE_CLIENT_EMAIL

# FIREBASE: FIREBASE_CLIENT_ID
echo "FIREBASE_CLIENT_ID:"
read FIREBASE_CLIENT_ID

# FIREBASE: FIREBASE_AUTH_URI
echo "FIREBASE_AUTH_URI:"
read FIREBASE_AUTH_URI

# FIREBASE: FIREBASE_TOKEN_URI
