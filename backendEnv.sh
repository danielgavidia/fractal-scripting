FIREBASE_AUTH_URI="$FIREBASE_AUTH_URI"
FIREBASE_TOKEN_URI="$FIREBASE_TOKEN_URI"
FIREBASE_AUTH_PROVIDER_CERT="$FIREBASE_AUTH_PROVIDER_CERT"
FIREBASE_CLIENT_CERT_URL="$FIREBASE_CLIENT_CERT_URL"
